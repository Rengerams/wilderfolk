import './canvasPolyfill';

/** In-memory localStorage so game init/preferences work under Node. */
class MemoryStorage implements Storage {
  private store = new Map<string, string>();

  get length() {
    return this.store.size;
  }

  clear(): void {
    this.store.clear();
  }

  getItem(key: string): string | null {
    return this.store.get(key) ?? null;
  }

  key(index: number): string | null {
    return [...this.store.keys()][index] ?? null;
  }

  removeItem(key: string): void {
    this.store.delete(key);
  }

  setItem(key: string, value: string): void {
    this.store.set(key, value);
  }
}

// Node 25+ exposes a broken experimental localStorage without --localstorage-file.
// Always substitute in tests (setup runs before game modules load in each worker).
globalThis.localStorage = new MemoryStorage();